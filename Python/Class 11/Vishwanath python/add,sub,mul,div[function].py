#To perform addition,multiplication,subtraction and division.
#"""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""
def add(a,b):
    c=a+b
    print(a,"+",b,"=",c)
def mul(a,b):
    c=a*b
    print(a,"X",b,"=",c)
def sub(a,b):
    c=a-b
    print(a,"-",b,"=",c)
def div(a,b):
    c=a/b
    print(a,"/",b,"=",c)
