#10-WAP to accept a character from the user and display whether it is a vowel or consonant.
a=str(input("Enter an English alphabet : "))
if a=="a" or a=="e" or a=="i" or a=="o" or a=="u" or a=="A" or a=="E" or a=="I" or a=="O" or a=="U":
    print(a,"is a vowel.")
else:
    print(a,"is a consonant.")
